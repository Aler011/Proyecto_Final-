    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        camino.clear();
        limpiar();
        if((Dini.getText()).equals("") || (Dfin.getText()).equals("")){
            JOptionPane.showMessageDialog(null, "No deje espacios en blanco");
        }else{
            int inicio = Integer.parseInt(Dini.getText());
            int fin = Integer.parseInt(Dfin.getText());
            int matriz[][];
            int c = 0, ban = 0;

            Grafo grafo = new Grafo(cant);
            matriz = grafo.matrizAdy(fil, col, aristas);

            //--- Empieza Dijkstra ---//
            for(int i = 0; i < cant; i++){
                if(i == inicio || i == fin){
                    ban++;
                }
            }
            if(ban == 2){
                dijkstra(matriz, inicio, fin);
                c = camino.size();
                for(int j = 0; j < c-1; j++){
                    ListNodos.get(camino.get(j)).dibujar(jPanel1.getGraphics(), camino.get(j), 2);
                try {
                        java.util.concurrent.TimeUnit.SECONDS.sleep(2);//Para ver el coso
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Ejecutar.class.getName()).log(Level.SEVERE, null, ex);
                    }
                for(int i = 0; i < aristas.size(); i++){          


                    //Pintar arista

                        if(aristas.get(i).getNi() == camino.get(j) && aristas.get(i).getNf() == camino.get(j+1)){
                            aristas.get(i).linea(jPanel1.getGraphics(), 2);
                        }else if(aristas.get(i).getNi() == camino.get(j+1) && aristas.get(i).getNf() == camino.get(j)){
                            aristas.get(i).linea(jPanel1.getGraphics(), 2);
                        }
                    }
                }
                //Pintamos el ultimo nodo a mano
               ListNodos.get(camino.get(c-1)).dibujar(jPanel1.getGraphics(), camino.get(c-1), 2); 
               
                JOptionPane.showMessageDialog(null, "Distancia: " + distanciaD);
                distanciaD = 0;
            }else{
                JOptionPane.showMessageDialog(null, "Nodo no existente");

            }
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

